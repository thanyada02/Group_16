#include <iostream>
using namespace std;

int main() {
  
  	// while loop to print numbers from 1 to 5
    int i = 0; // initialize counter variable
    while (i < 5) { // condition for the loop to continue
        cout << "Hi  => i = " << i << endl; // output the current value of i
        i++; // increment i by 1
    }

    return 0; // end of the program
}
